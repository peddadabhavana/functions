
# Here we have defined a function to calculate the average of 3 numbers
def average(a,b,c):
    avg=a+b+c/3;
    return avg  
# Here we are calling the function and passing the values of a,b,c as arguments
print(average(20,34,86))
print(int(average(50,34,80)))
print(int(average(1,2,3)))

